package loops_working;

public class ExerciseLoop {

    public static void main(String[] args) {

        // SYNTAX :   for ( variable declaration ; condition ; increment/decrement ){ code }

        for (int i=1; i<=10 ; i++ ){
            int two=2;
            int output= two*i;
            System.out.println(output);
        }

        for (int i=1; i<=10 ; i++ ){
            int five=5;
            int output= five*i;
            System.out.println(output);
        }



    }
}
